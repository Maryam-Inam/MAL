    public void updateFeed2(){
           int size = currentUser.newsFeed.post.posts.size();
        System.out.println("POSTS: " + size);
        
        //array of posts 
        String[] tempPost = new String[size];
        
        for(int i = 0; i< size; i++){
            tempPost[i] = currentUser.newsFeed.post.posts.get(i).text;
        }
        
        //array of names of users who posted the post
        String[] tempUsers = new String[size];
        
        for(int i = 0; i< size; i++){
            tempUsers[i] = currentUser.newsFeed.post.posts.get(i).sentBy;
        }
        //final string to be populated
        String[] strings = new String[size*2];
        
        
        //combining above two arrays
        for(int i =0; i< size ; i++){
            
            //checks if the post is done by the current user then it replaces the sent by name with me
            if(tempUsers[i].equals(currentUser.username))
                strings[i*2] = "Me >>";
            else
            strings[i*2] = tempUsers[i] + " >>";
            
            
            strings[(i*2)+1] = "                "+ tempPost[i];
        }
        
        feed.setModel(new javax.swing.AbstractListModel<String>() {
            
            public int getSize() { return strings.length; }
            public String getElementAt(int i) { return strings[i]; }
        });
    }
