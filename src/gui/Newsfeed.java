    private void suggestedValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_suggestedValueChanged
        // TODO add your handling code here:
        String data = "";
         if (!evt.getValueIsAdjusting()) {
                  System.out.println(suggested.getSelectedIndex() + suggested.getSelectedValue().toString());
                  data = suggested.getSelectedValue().toString();
                  data += "\n" + "Studies At:\n" + 
                          currentUser.suggestions.suggestFriends(currentUser).get(suggested.getSelectedIndex()).institute + "\n" + "Lives:\n"+
                          currentUser.suggestions.suggestFriends(currentUser).get(suggested.getSelectedIndex()).city + "\n" + "Relationship Status:\n";
                  if(currentUser.suggestions.suggestFriends(currentUser).get(suggested.getSelectedIndex()).maritalStatus == null)
                      data += "Not Available";
                  else
                      data += currentUser.suggestions.suggestFriends(currentUser).get(suggested.getSelectedIndex()).maritalStatus;
                  
                  userDetails.setText(data);
                  
                  addFriend.setEnabled(true);
                  
                  if(!currentUser.followingList.following.containsKey(currentUser.suggestions.suggestFriends(currentUser).get(suggested.getSelectedIndex()).name))
                  followFriend.setEnabled(true);
                }
        
    }//GEN-LAST:event_suggestedValueChanged

