    private void alreadyMemberMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_alreadyMemberMouseEntered
        // TODO add your handling code here:
       Font font = alreadyMember.getFont();
        Map<TextAttribute, Object> attributes = new HashMap<>(font.getAttributes());
        attributes.put(TextAttribute.UNDERLINE, TextAttribute.UNDERLINE_ON);
        alreadyMember.setFont(font.deriveFont(attributes));
        alreadyMember.setForeground(Color.black);
    }//GEN-LAST:event_alreadyMemberMouseEntered

    private void alreadyMemberMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_alreadyMemberMouseExited
        // TODO add your handling code here:
         Font font = alreadyMember.getFont();
        Map<TextAttribute, Object> attributes = new HashMap<>(font.getAttributes());
        attributes.put(TextAttribute.UNDERLINE, -1);
        alreadyMember.setFont(font.deriveFont(attributes));
        alreadyMember.setForeground(Color.GRAY);
    }//GEN-LAST:event_alreadyMemberMouseExited

