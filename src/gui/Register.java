    private void createAccBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createAccBtnMouseClicked
        // TODO add your handling code here:
        try{
            if(password.getText().equals("") || username.getText().equals(""))
                throw new Exception();
        user=new User(name.getText(),  Integer.parseInt(phone.getText()), city.getText(), institute.getText(), username.getText(), password.getText());
        
        JOptionPane.showMessageDialog(this, "Registered Successfully");
        
        }
        
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Enter correct phone number");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Enter complete information");
            //e.printStackTrace();
        }
        
    }//GEN-LAST:event_createAccBtnMouseClicked

